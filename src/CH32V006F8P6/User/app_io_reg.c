static uint8_t s_app_io_reg[APP_IO_REG_NUM] = {0};

// -----------------------------------------------------------
// [Static関数]

// -----------------------------------------------------------
// [API]

void app_io_reg_init(void)
{
    uint8_t i;

    for(i = 0; i < APP_IO_REG_NUM; i++)
    {
        s_app_io_reg[i] = g_app_io_reg[i].reg_init_val;
    }
}

uint8_t app_io_reg_read(uint8_t addr)
{
    uint8_t reg;

    if(addr < APP_IO_REG_NUM) {
        reg = s_app_io_reg[addr] & g_app_io_reg[addr].read_bit;
    } else {
        reg = 0x00;
    }

    return reg;
}

void app_io_reg_write(uint8_t addr, uint8_t val)
{
    if(addr < APP_IO_REG_NUM) {
        s_app_io_reg[addr] = val & g_app_io_reg[addr].write_bit;
    }
}